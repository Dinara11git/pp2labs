# for_loop.py
# Примеры обычного for цикла

fruits = ["apple", "banana", "cherry"]

# Example 1: перебор списка
for x in fruits:
    print(x)

# Example 2: перебор строки
for x in "banana":
    print(x)

# Example 3: использование range()
for x in range(6):
    print(x)

# Example 4: range с началом и концом
for x in range(2, 6):
    print(x)

# Example 5: range с шагом
for x in range(2, 30, 3):
    print(x)
